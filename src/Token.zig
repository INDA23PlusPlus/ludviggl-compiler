
pub const Kind = enum {

    identifier, // int, var
    literal,    // 1232

    @"+",       // +
    @"-",       // -
    @"*",       // *
    @"/",       // /
    @"=",       // =
    @">",       // >
    @"<",       // <
    @"!",       // !

    @"+=",      // +=
    @"-=",      // -=
    @"*=",      // *=
    @"/=",      // /=
    @"==",      // ==
    @">=",      // >=
    @"<=",      // <=
    @"!=",      // !=

    @";",       // ;
    @":",       // :
    @"(",       // (
    @")",       // )
    @"{",       // {
    @"}",       // }

    @"if",      // if
    @"while",   // while
    @"else",    // else
    @"let",     // let

    err,
};

kind:  Kind,
begin: usize,
end:   usize,
